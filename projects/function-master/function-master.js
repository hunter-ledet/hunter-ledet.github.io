//////////////////////////////////////////////////////////////////////
// Function 2 - Keys to String ///////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function keysToString(object) {
    var newArr = [];
   // var newStr = "";
    for(let key in object){
     newArr.push(object[key].key);    
    }
    let newStr = newArr.join(' ');
    return newStr;
}

